public class Literals {
    public static void main(String[] args) {
        byte age = 34; // (Here age is integer literal);
        char ch = 'A';
        float f1 = 4.3f;
        double d = 4.66; // we can also use d = 4.66D
        System.out.println(age + "\t" + ch + "\t" + f1 + "\t" + d);
        String str = "Shashank Sharma";
        System.out.println(str);
    }
}
